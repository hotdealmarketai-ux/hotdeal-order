// systemExtra: 재고 조회 결과 등 그때그때 상황을 시스템 프롬프트에 덧붙일 때 사용(호출자가 사실을 주입).
export async function askAssistant(
  history: AssistantMsg[],
  systemExtra = "",
): Promise<string> {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    return "지금은 AI 도우미를 이용할 수 없어요. 위 '관리자 문의' 탭에서 직접 여쭤봐 주세요.";
  }
  try {
    const { default: Anthropic } = await import("@anthropic-ai/sdk");
    // 타임아웃 미지정 시 SDK 기본 10분 — 답이 늦으면 도우미 화면이 계속 대기 상태가 된다.
    const client = new Anthropic({ apiKey, timeout: 15_000, maxRetries: 1 });
    const model = process.env.ANTHROPIC_MODEL || "claude-sonnet-4-6";
    // 이력은 user/assistant 교대라 그냥 자르면 첫 메시지가 assistant가 될 수 있고,
    // 그러면 API가 400을 내 세션 내내 답변이 끊긴다(대화가 길어지면 항상 발생).
    // → 자른 뒤 앞쪽 assistant를 버려 반드시 user로 시작하게 한다.
    const trimmed = history.slice(-12);
    while (trimmed.length > 0 && trimmed[0].role !== "user") trimmed.shift();
    const msg = await client.messages.create({
      model,
      max_tokens: 700,
      system: systemExtra ? `${APP_GUIDE}\n\n${systemExtra}` : APP_GUIDE,
      messages: trimmed.map((m) => ({ role: m.role, content: m.content })),
    });
    const text = msg.content
      .map((b) => (b.type === "text" ? b.text : ""))
      .join("")
      .trim();
    return text || "다시 한 번 여쭤봐 주세요.";
  } catch (err) {
    logError("assistant.ask", err, {});
    return "답변 중 문제가 생겼어요. 잠시 후 다시 시도하거나 '관리자 문의' 탭으로 여쭤봐 주세요.";
  }
}
