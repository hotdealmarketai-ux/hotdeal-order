-- 주간발주 상품 카탈로그 DB화(WeeklyProduct) + WeeklyOrder 상태(confirmed) + WeeklyPrice 제거

-- WeeklyOrder 상태 컬럼
ALTER TABLE "WeeklyOrder" ADD COLUMN     "confirmed" BOOLEAN NOT NULL DEFAULT false,
ADD COLUMN     "confirmedAt" TIMESTAMP(3);

-- 상품 카탈로그 테이블
CREATE TABLE "WeeklyProduct" (
    "code" TEXT NOT NULL,
    "category" TEXT NOT NULL,
    "name" TEXT NOT NULL,
    "perBox" INTEGER NOT NULL DEFAULT 1,
    "supplyPrice" INTEGER NOT NULL DEFAULT 0,
    "sortOrder" INTEGER NOT NULL DEFAULT 0,
    "active" BOOLEAN NOT NULL DEFAULT true,
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL,

    CONSTRAINT "WeeklyProduct_pkey" PRIMARY KEY ("code")
);
CREATE INDEX "WeeklyProduct_active_sortOrder_idx" ON "WeeklyProduct"("active", "sortOrder");

